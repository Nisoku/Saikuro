//! Python binding generator.
//!
//! Produces:
//! - A `types.py` with dataclasses for all named types.
//! - One `<namespace>_client.py` per namespace with a typed async client class.
//! - A `__init__.py` that re-exports everything.

use saikuro_core::schema::{
    FunctionSchema, NamespaceSchema, PrimitiveType, Schema, TypeDefinition, TypeDescriptor,
    Visibility,
};

use crate::{
    error::Result,
    generator::{BindingGenerator, GeneratorOutput},
};

pub struct PythonGenerator;

impl BindingGenerator for PythonGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();

        // types.py
        let types_src = self.generate_types(schema)?;
        output.add("types.py", types_src);

        // per-namespace client stubs
        let mut init_imports = vec!["from .types import *".to_owned()];

        for (ns_name, ns_schema) in &schema.namespaces {
            let file_name = format!("{}_client.py", ns_name);
            let class_name = to_pascal_case(ns_name);
            let src = self.generate_namespace_client(ns_name, &class_name, ns_schema)?;
            output.add(&file_name, src);
            init_imports.push(format!(
                "from .{}_client import {}Client",
                ns_name, class_name
            ));
        }

        // __init__.py
        output.add("__init__.py", init_imports.join("\n") + "\n");

        Ok(output)
    }
}

impl PythonGenerator {
    fn generate_types(&self, schema: &Schema) -> Result<String> {
        let mut lines = vec![
            "# AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "from __future__ import annotations".to_owned(),
            "from dataclasses import dataclass, field".to_owned(),
            "from typing import Optional, List, Dict, Any".to_owned(),
            String::new(),
        ];

        for (type_name, type_def) in &schema.types {
            match type_def {
                TypeDefinition::Record { fields } => {
                    lines.push("@dataclass".to_string());
                    lines.push(format!("class {}:", type_name));
                    if fields.is_empty() {
                        lines.push("    pass".to_owned());
                    } else {
                        for (field_name, field_desc) in fields {
                            let py_type = self.type_to_python(&field_desc.r#type)?;
                            let default = if field_desc.optional {
                                format!(": Optional[{}] = None", py_type)
                            } else {
                                format!(": {}", py_type)
                            };
                            lines.push(format!("    {}{}", field_name, default));
                        }
                    }
                    lines.push(String::new());
                }
                TypeDefinition::Enum { variants } => {
                    lines.push("from enum import Enum".to_owned());
                    lines.push(format!("class {}(str, Enum):", type_name));
                    for variant in variants {
                        lines.push(format!("    {} = \"{}\"", variant, variant));
                    }
                    lines.push(String::new());
                }
                TypeDefinition::Alias { inner } => {
                    let py_type = self.type_to_python(inner)?;
                    lines.push(format!("{} = {}", type_name, py_type));
                    lines.push(String::new());
                }
            }
        }

        Ok(lines.join("\n"))
    }

    fn generate_namespace_client(
        &self,
        ns_name: &str,
        class_name: &str,
        ns: &NamespaceSchema,
    ) -> Result<String> {
        // Determine whether any function uses stream or channel returns so we
        // know whether to import SaikuroStream / SaikuroChannel.
        let needs_stream = ns
            .functions
            .values()
            .any(|f| matches!(f.returns, TypeDescriptor::Stream { .. }));
        let needs_channel = ns
            .functions
            .values()
            .any(|f| matches!(f.returns, TypeDescriptor::Channel { .. }));

        let mut stream_imports: Vec<&str> = vec![];
        if needs_stream {
            stream_imports.push("SaikuroStream");
        }
        if needs_channel {
            stream_imports.push("SaikuroChannel");
        }

        let mut lines = vec![
            "# AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "from __future__ import annotations".to_owned(),
            "from typing import Optional, List, Dict, Any, AsyncIterator".to_owned(),
            "from .types import *".to_owned(),
            "# Import your Saikuro Python adapter here:".to_owned(),
            "# from saikuro import SaikuroClient".to_owned(),
        ];

        if !stream_imports.is_empty() {
            lines.push(format!(
                "# from saikuro import {}",
                stream_imports.join(", ")
            ));
        }

        lines.push(String::new());
        lines.push(format!("class {}Client:", class_name));
        lines.push(format!(
            "    \"\"\"Typed client for the '{}' namespace.\"\"\"",
            ns_name
        ));
        lines.push(String::new());
        lines.push("    def __init__(self, client: Any) -> None:".to_owned());
        lines.push("        self._client = client".to_owned());
        lines.push(String::new());

        for (fn_name, fn_schema) in &ns.functions {
            if fn_schema.visibility == Visibility::Private {
                continue;
            }

            let method = self.generate_method(ns_name, fn_name, fn_schema)?;
            lines.push(method);
        }

        Ok(lines.join("\n"))
    }

    fn generate_method(
        &self,
        ns_name: &str,
        fn_name: &str,
        schema: &FunctionSchema,
    ) -> Result<String> {
        let mut args = vec!["self".to_owned()];
        let mut call_args = vec![];

        for arg in &schema.args {
            let py_type = self.type_to_python(&arg.r#type)?;
            if arg.optional {
                args.push(format!("{}: Optional[{}] = None", arg.name, py_type));
            } else {
                args.push(format!("{}: {}", arg.name, py_type));
            }
            call_args.push(arg.name.clone());
        }

        let return_type = self.type_to_python(&schema.returns)?;
        let target = format!("{}.{}", ns_name, fn_name);
        let args_list = call_args.join(", ");

        // Choose the correct client primitive based on the return type.
        let (signature, body) = match &schema.returns {
            TypeDescriptor::Stream { .. } => (
                format!(
                    "    async def {}({}) -> {}:",
                    fn_name,
                    args.join(", "),
                    return_type
                ),
                format!(
                    "        return await self._client.stream(\"{}\", [{}])",
                    target, args_list
                ),
            ),
            TypeDescriptor::Channel { inbound, outbound } => {
                let in_py = self.type_to_python(inbound)?;
                let out_py = self.type_to_python(outbound)?;
                (
                    format!(
                        "    async def {}({}) -> \"SaikuroChannel[{}, {}]\":",
                        fn_name,
                        args.join(", "),
                        in_py,
                        out_py,
                    ),
                    format!(
                        "        return await self._client.channel(\"{}\", [{}])",
                        target, args_list
                    ),
                )
            }
            _ => (
                format!(
                    "    async def {}({}) -> {}:",
                    fn_name,
                    args.join(", "),
                    return_type
                ),
                format!(
                    "        return await self._client.call(\"{}\", [{}])",
                    target, args_list
                ),
            ),
        };

        let mut lines = vec![];
        if let Some(doc) = &schema.doc {
            lines.push(format!("    \"\"\"{}\"\"\"", doc));
        }
        lines.push(signature);
        lines.push(body);
        lines.push(String::new());

        Ok(lines.join("\n"))
    }

    fn type_to_python(&self, desc: &TypeDescriptor) -> Result<String> {
        Ok(match desc {
            TypeDescriptor::Primitive { r#type } => match r#type {
                PrimitiveType::Bool => "bool".to_owned(),
                PrimitiveType::I8
                | PrimitiveType::I16
                | PrimitiveType::I32
                | PrimitiveType::I64
                | PrimitiveType::U8
                | PrimitiveType::U16
                | PrimitiveType::U32
                | PrimitiveType::U64 => "int".to_owned(),
                PrimitiveType::F32 | PrimitiveType::F64 => "float".to_owned(),
                PrimitiveType::String => "str".to_owned(),
                PrimitiveType::Bytes => "bytes".to_owned(),
                PrimitiveType::Any => "Any".to_owned(),
                PrimitiveType::Unit => "None".to_owned(),
            },
            TypeDescriptor::Named { name } => name.clone(),
            TypeDescriptor::Option { inner } => {
                format!("Optional[{}]", self.type_to_python(inner)?)
            }
            TypeDescriptor::Array { item } => {
                format!("List[{}]", self.type_to_python(item)?)
            }
            TypeDescriptor::Map { value } => {
                format!("Dict[str, {}]", self.type_to_python(value)?)
            }
            TypeDescriptor::Stream { item } => {
                format!("AsyncIterator[{}]", self.type_to_python(item)?)
            }
            TypeDescriptor::Channel { inbound, outbound } => {
                format!(
                    "\"SaikuroChannel[{}, {}]\"",
                    self.type_to_python(inbound)?,
                    self.type_to_python(outbound)?
                )
            }
        })
    }
}

fn to_pascal_case(s: &str) -> String {
    s.split('_')
        .map(|part| {
            let mut c = part.chars();
            match c.next() {
                None => String::new(),
                Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
            }
        })
        .collect()
}
