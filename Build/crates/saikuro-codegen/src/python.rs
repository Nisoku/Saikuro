//! Python binding generator.
//!
//! Produces:
//! - A `types.py` with dataclasses for all named types.
//! - One `<namespace>_client.py` per namespace with a typed async client class.
//! - A `__init__.py` that re-exports everything.

use saikuro_core::schema::{
    FunctionSchema, NamespaceSchema, PrimitiveType, Schema, TypeDescriptor,
};

use crate::{
    error::Result,
    generator::{
        convert_type, generate_types_and_namespace_clients, generate_types_from_schema,
        BindingGenerator, GeneratorOutput, TypeConverter,
    },
    to_pascal_case,
};

pub struct PythonGenerator;

impl BindingGenerator for PythonGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();
        let ns_pairs = generate_types_and_namespace_clients(
            schema,
            &mut output,
            "types.py",
            self.generate_types(schema)?,
            |ns| format!("{}_client.py", ns),
            to_pascal_case,
            |ns, class_name, ns_schema| self.generate_namespace_client(ns, class_name, ns_schema),
        )?;
        let mut imports = vec!["from .types import *".to_owned()];
        for (ns_name, class_name) in &ns_pairs {
            imports.push(format!("from .{ns_name}_client import {class_name}Client"));
        }
        output.add("__init__.py", imports.join("\n") + "\n");
        Ok(output)
    }
}

impl PythonGenerator {
    fn generate_types(&self, schema: &Schema) -> Result<String> {
        let header = vec![
            "# AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "from __future__ import annotations".to_owned(),
            "from dataclasses import dataclass, field".to_owned(),
            "from enum import Enum".to_owned(),
            "from typing import Optional, List, Dict, Any".to_owned(),
            String::new(),
        ];

        let type_to_py = |desc: &TypeDescriptor| self.type_to_python(desc);

        generate_types_from_schema(
            schema,
            header,
            |type_name, fields| {
                let mut lines = vec![format!("@dataclass"), format!("class {type_name}:")];
                if fields.is_empty() {
                    lines.push("    pass".to_owned());
                } else {
                    for (field_name, field_desc) in fields {
                        let py_type = type_to_py(&field_desc.r#type);
                        let default = if field_desc.optional {
                            format!(": Optional[{py_type}] = None")
                        } else {
                            format!(": {py_type}")
                        };
                        lines.push(format!("    {field_name}{default}"));
                    }
                }
                lines.push(String::new());
                Ok(lines)
            },
            |type_name, variants| {
                let mut lines = vec![format!("class {type_name}(str, Enum):")];
                for variant in variants {
                    lines.push(format!("    {variant} = \"{variant}\""));
                }
                lines.push(String::new());
                Ok(lines)
            },
            |type_name, inner| {
                let py_type = type_to_py(inner);
                Ok(vec![format!("{type_name} = {py_type}"), String::new()])
            },
        )
    }

    fn generate_namespace_client(
        &self,
        ns_name: &str,
        class_name: &str,
        ns: &NamespaceSchema,
    ) -> Result<String> {
        // Determine whether any function uses stream or channel returns so we
        // know whether to import SaikuroStream / SaikuroChannel.
        let needs_stream = ns
            .functions
            .values()
            .any(|f| matches!(f.returns, TypeDescriptor::Stream { .. }));
        let needs_channel = ns
            .functions
            .values()
            .any(|f| matches!(f.returns, TypeDescriptor::Channel { .. }));

        let mut stream_imports: Vec<&str> = vec![];
        if needs_stream {
            stream_imports.push("SaikuroStream");
        }
        if needs_channel {
            stream_imports.push("SaikuroChannel");
        }

        let mut lines = vec![
            "# AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "from __future__ import annotations".to_owned(),
            "from typing import Optional, List, Dict, Any, AsyncIterator".to_owned(),
            "from .types import *".to_owned(),
            "# Import your Saikuro Python adapter here:".to_owned(),
            "# from saikuro import SaikuroClient".to_owned(),
        ];

        if !stream_imports.is_empty() {
            lines.push(format!(
                "# from saikuro import {}",
                stream_imports.join(", ")
            ));
        }

        lines.push(String::new());
        lines.push(format!("class {class_name}Client:"));
        lines.push(format!(
            "    \"\"\"Typed client for the '{ns_name}' namespace.\"\"\""
        ));
        lines.push(String::new());
        lines.push("    def __init__(self, client: Any) -> None:".to_owned());
        lines.push("        self._client = client".to_owned());
        lines.push(String::new());

        for (fn_name, fn_schema) in crate::generator::namespace_public_functions(ns) {
            let method = self.generate_method(ns_name, fn_name, fn_schema)?;
            lines.push(method);
        }

        Ok(lines.join("\n"))
    }

    fn generate_method(
        &self,
        ns_name: &str,
        fn_name: &str,
        schema: &FunctionSchema,
    ) -> Result<String> {
        let mut args = vec!["self".to_owned()];
        let mut call_args = vec![];

        for arg in &schema.args {
            let py_type = self.type_to_python(&arg.r#type);
            if arg.optional {
                args.push(format!("{}: Optional[{}] = None", arg.name, py_type));
            } else {
                args.push(format!("{}: {}", arg.name, py_type));
            }
            call_args.push(arg.name.clone());
        }

        let return_type = self.type_to_python(&schema.returns);
        let target = format!("{ns_name}.{fn_name}");
        let args_list = call_args.join(", ");

        // Choose the correct client primitive based on the return type.
        let (signature, body) = match &schema.returns {
            TypeDescriptor::Stream { .. } => (
                format!(
                    "    async def {}({}) -> {}:",
                    fn_name,
                    args.join(", "),
                    return_type
                ),
                format!("        return await self._client.stream(\"{target}\", [{args_list}])"),
            ),
            TypeDescriptor::Channel { inbound, outbound } => {
                let in_py = self.type_to_python(inbound);
                let out_py = self.type_to_python(outbound);
                (
                    format!(
                        "    async def {}({}) -> \"SaikuroChannel[{}, {}]\":",
                        fn_name,
                        args.join(", "),
                        in_py,
                        out_py,
                    ),
                    format!(
                        "        return await self._client.channel(\"{target}\", [{args_list}])"
                    ),
                )
            }
            _ => (
                format!(
                    "    async def {}({}) -> {}:",
                    fn_name,
                    args.join(", "),
                    return_type
                ),
                format!("        return await self._client.call(\"{target}\", [{args_list}])"),
            ),
        };

        let mut lines = vec![];
        if let Some(doc) = &schema.doc {
            lines.push(format!("    \"\"\"{doc}\"\"\""));
        }
        lines.push(signature);
        lines.push(body);
        lines.push(String::new());

        Ok(lines.join("\n"))
    }

    fn type_to_python(&self, desc: &TypeDescriptor) -> String {
        convert_type(desc, self)
    }
}

impl TypeConverter for PythonGenerator {
    fn primitive_name(&self, t: &PrimitiveType) -> &'static str {
        match t {
            PrimitiveType::Bool => "bool",
            PrimitiveType::I8
            | PrimitiveType::I16
            | PrimitiveType::I32
            | PrimitiveType::I64
            | PrimitiveType::U8
            | PrimitiveType::U16
            | PrimitiveType::U32
            | PrimitiveType::U64 => "int",
            PrimitiveType::F32 | PrimitiveType::F64 => "float",
            PrimitiveType::String => "str",
            PrimitiveType::Bytes => "bytes",
            PrimitiveType::Any => "Any",
            PrimitiveType::Unit => "None",
        }
    }

    fn named_type(&self, name: &str) -> String {
        name.to_owned()
    }

    fn wrap_option(&self, inner: &str) -> String {
        format!("Optional[{inner}]")
    }

    fn wrap_array(&self, inner: &str) -> String {
        format!("List[{inner}]")
    }

    fn wrap_map(&self, value: &str) -> String {
        format!("Dict[str, {value}]")
    }

    fn wrap_stream(&self, item: &str) -> String {
        format!("AsyncIterator[{item}]")
    }

    fn wrap_channel(&self, inbound: &str, outbound: &str) -> String {
        format!("\"SaikuroChannel[{inbound}, {outbound}]\"")
    }
}
