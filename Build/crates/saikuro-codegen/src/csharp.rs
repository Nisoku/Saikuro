//! C# binding generator.
//!
//! Produces:
//! - A `Types.cs` with records for all named types.
//! - One `<Namespace>Client.cs` per namespace with a typed async client class.
//! - A `Generated.cs` that lists each client class (summary comment).

use saikuro_core::schema::{
    FunctionSchema, NamespaceSchema, PrimitiveType, Schema, TypeDefinition, TypeDescriptor,
    Visibility,
};

use crate::{
    error::Result,
    generator::{BindingGenerator, GeneratorOutput},
};

pub struct CSharpGenerator;

impl BindingGenerator for CSharpGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();

        // Types.cs
        let types_src = self.generate_types(schema)?;
        output.add("Types.cs", types_src);

        // per-namespace client files
        let mut client_names: Vec<String> = vec![];

        for (ns_name, ns_schema) in &schema.namespaces {
            let class_name = format!("{}Client", to_pascal_case(ns_name));
            let file_name = format!("{}.cs", class_name);
            let src = self.generate_namespace_client(ns_name, &class_name, ns_schema)?;
            output.add(&file_name, src);
            client_names.push(class_name);
        }

        // Generated.cs: a simple index listing all clients
        let generated_src = self.generate_index(&client_names);
        output.add("Generated.cs", generated_src);

        Ok(output)
    }
}

impl CSharpGenerator {
    fn generate_types(&self, schema: &Schema) -> Result<String> {
        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "".to_owned(),
            "namespace Saikuro.Generated;".to_owned(),
            "".to_owned(),
        ];

        for (type_name, type_def) in &schema.types {
            match type_def {
                TypeDefinition::Record { fields } => {
                    lines.push(format!("public sealed record {}(", type_name));
                    let field_lines: Vec<String> = fields
                        .iter()
                        .map(|(field_name, field_desc)| {
                            let cs_type = self
                                .type_to_cs(&field_desc.r#type)
                                .unwrap_or_else(|_| "object?".to_owned());
                            let nullable = if field_desc.optional {
                                format!("{}?", cs_type)
                            } else {
                                cs_type
                            };
                            let pascal = to_pascal_case(field_name);
                            format!("    {} {}", nullable, pascal)
                        })
                        .collect();
                    lines.push(field_lines.join(",\n"));
                    lines.push(");".to_owned());
                    lines.push("".to_owned());
                }
                TypeDefinition::Enum { variants } => {
                    lines.push(format!("public enum {} {{", type_name));
                    for variant in variants {
                        lines.push(format!("    {},", variant));
                    }
                    lines.push("}".to_owned());
                    lines.push("".to_owned());
                }
                TypeDefinition::Alias { inner } => {
                    // C# doesn't have true type aliases; emit a using directive.
                    let cs_type = self.type_to_cs(inner)?;
                    lines.push(format!("// {} is an alias for {}", type_name, cs_type));
                    lines.push(format!("using {} = {};", type_name, cs_type));
                    lines.push("".to_owned());
                }
            }
        }

        Ok(lines.join("\n"))
    }

    fn generate_namespace_client(
        &self,
        ns_name: &str,
        class_name: &str,
        ns: &NamespaceSchema,
    ) -> Result<String> {
        let needs_stream = ns
            .functions
            .values()
            .any(|f| matches!(f.returns, TypeDescriptor::Stream { .. }));
        let needs_channel = ns
            .functions
            .values()
            .any(|f| matches!(f.returns, TypeDescriptor::Channel { .. }));

        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "".to_owned(),
            "using Saikuro;".to_owned(),
            "using Saikuro.Generated;".to_owned(),
        ];

        if needs_stream {
            lines.push("// SaikuroStream<T> is provided by the Saikuro adapter.".to_owned());
        }
        if needs_channel {
            lines.push(
                "// SaikuroChannel<TIn, TOut> is provided by the Saikuro adapter.".to_owned(),
            );
        }

        lines.push("".to_owned());
        lines.push("namespace Saikuro.Generated;".to_owned());
        lines.push("".to_owned());

        if let Some(doc) = &ns.doc {
            lines.push(format!("/// <summary>{}</summary>", doc));
        }

        lines.push(format!("public sealed class {} {{", class_name));
        lines.push("    private readonly SaikuroClient _client;".to_owned());
        lines.push("".to_owned());
        lines.push(format!(
            "    public {}(SaikuroClient client) => _client = client;",
            class_name
        ));
        lines.push("".to_owned());

        for (fn_name, fn_schema) in &ns.functions {
            if fn_schema.visibility == Visibility::Private {
                continue;
            }

            let method = self.generate_method(ns_name, fn_name, fn_schema)?;
            lines.push(method);
        }

        lines.push("}".to_owned());
        lines.push("".to_owned());

        Ok(lines.join("\n"))
    }

    fn generate_method(
        &self,
        ns_name: &str,
        fn_name: &str,
        schema: &FunctionSchema,
    ) -> Result<String> {
        let method_name = to_pascal_case(fn_name);
        let target = format!("{}.{}", ns_name, fn_name);

        let mut params: Vec<String> = vec![];
        let mut call_args_items: Vec<String> = vec![];

        for arg in &schema.args {
            let cs_type = self.type_to_cs(&arg.r#type)?;
            if arg.optional {
                params.push(format!("{}? {} = null", cs_type, to_camel_case(&arg.name)));
            } else {
                params.push(format!("{} {}", cs_type, to_camel_case(&arg.name)));
            }
            call_args_items.push(to_camel_case(&arg.name));
        }

        let params_str = params.join(", ");
        let args_array = if call_args_items.is_empty() {
            "Array.Empty<object?>()".to_owned()
        } else {
            format!("new object?[] {{ {} }}", call_args_items.join(", "))
        };

        let mut lines = vec![];

        if let Some(doc) = &schema.doc {
            lines.push(format!("    /// <summary>{}</summary>", doc));
        }

        match &schema.returns {
            TypeDescriptor::Stream { item } => {
                let item_cs = self.type_to_cs(item)?;
                lines.push(format!(
                    "    public Task<SaikuroStream<{}>> {}Async({}) =>",
                    item_cs, method_name, params_str
                ));
                lines.push(format!(
                    "        _client.StreamAsync<{}>(\"{}\",",
                    item_cs, target
                ));
                lines.push(format!("            {});", args_array));
            }
            TypeDescriptor::Channel { inbound, outbound } => {
                let in_cs = self.type_to_cs(inbound)?;
                let out_cs = self.type_to_cs(outbound)?;
                lines.push(format!(
                    "    public Task<SaikuroChannel<{}, {}>> {}Async({}) =>",
                    in_cs, out_cs, method_name, params_str
                ));
                lines.push(format!(
                    "        _client.ChannelAsync<{}, {}>(\"{}\",",
                    in_cs, out_cs, target
                ));
                lines.push(format!("            {});", args_array));
            }
            _ => {
                let return_cs = self.type_to_cs(&schema.returns)?;
                if return_cs == "void" {
                    lines.push(format!(
                        "    public async Task {}Async({}) {{",
                        method_name, params_str
                    ));
                    lines.push(format!(
                        "        await _client.CallAsync(\"{}\", {});",
                        target, args_array
                    ));
                    lines.push("    }".to_owned());
                } else {
                    lines.push(format!(
                        "    public async Task<{}> {}Async({}) {{",
                        return_cs, method_name, params_str
                    ));
                    lines.push(format!(
                        "        var result = await _client.CallAsync(\"{}\", {});",
                        target, args_array
                    ));
                    lines.push(format!("        return ({})result!;", return_cs));
                    lines.push("    }".to_owned());
                }
            }
        }

        lines.push("".to_owned());

        Ok(lines.join("\n"))
    }

    fn generate_index(&self, client_names: &[String]) -> String {
        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "//".to_owned(),
            "// Generated clients:".to_owned(),
        ];
        for name in client_names {
            lines.push(format!("//   {}", name));
        }
        lines.push("".to_owned());
        lines.join("\n")
    }

    fn type_to_cs(&self, desc: &TypeDescriptor) -> Result<String> {
        Ok(match desc {
            TypeDescriptor::Primitive { r#type } => match r#type {
                PrimitiveType::Bool => "bool".to_owned(),
                PrimitiveType::I8 => "sbyte".to_owned(),
                PrimitiveType::I16 => "short".to_owned(),
                PrimitiveType::I32 => "int".to_owned(),
                PrimitiveType::I64 => "long".to_owned(),
                PrimitiveType::U8 => "byte".to_owned(),
                PrimitiveType::U16 => "ushort".to_owned(),
                PrimitiveType::U32 => "uint".to_owned(),
                PrimitiveType::U64 => "ulong".to_owned(),
                PrimitiveType::F32 => "float".to_owned(),
                PrimitiveType::F64 => "double".to_owned(),
                PrimitiveType::String => "string".to_owned(),
                PrimitiveType::Bytes => "byte[]".to_owned(),
                PrimitiveType::Any => "object?".to_owned(),
                PrimitiveType::Unit => "void".to_owned(),
            },
            TypeDescriptor::Named { name } => name.clone(),
            TypeDescriptor::Option { inner } => {
                let inner_cs = self.type_to_cs(inner)?;
                format!("{}?", inner_cs)
            }
            TypeDescriptor::Array { item } => {
                format!("List<{}>", self.type_to_cs(item)?)
            }
            TypeDescriptor::Map { value } => {
                format!("Dictionary<string, {}>", self.type_to_cs(value)?)
            }
            TypeDescriptor::Stream { item } => {
                format!("SaikuroStream<{}>", self.type_to_cs(item)?)
            }
            TypeDescriptor::Channel { inbound, outbound } => {
                format!(
                    "SaikuroChannel<{}, {}>",
                    self.type_to_cs(inbound)?,
                    self.type_to_cs(outbound)?
                )
            }
        })
    }
}

fn to_pascal_case(s: &str) -> String {
    s.split('_')
        .map(|part| {
            let mut c = part.chars();
            match c.next() {
                None => String::new(),
                Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
            }
        })
        .collect()
}

fn to_camel_case(s: &str) -> String {
    let pascal = to_pascal_case(s);
    let mut c = pascal.chars();
    match c.next() {
        None => String::new(),
        Some(f) => f.to_lowercase().collect::<String>() + c.as_str(),
    }
}
