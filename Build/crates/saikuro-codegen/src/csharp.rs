//! C# binding generator.
//!
//! Produces:
//! - A `Types.cs` with records for all named types.
//! - One `<Namespace>Client.cs` per namespace with a typed async client class.
//! - A `Generated.cs` that lists each client class (summary comment).

use saikuro_core::schema::{
    FunctionSchema, NamespaceSchema, PrimitiveType, Schema, TypeDescriptor, Visibility,
};

use crate::{
    error::Result,
    generator::{
        generate_types_and_namespace_clients, generate_types_from_schema, BindingGenerator,
        GeneratorOutput,
    },
    to_camel_case, to_pascal_case,
};

pub struct CSharpGenerator;

impl BindingGenerator for CSharpGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();
        let ns_pairs = generate_types_and_namespace_clients(
            schema,
            &mut output,
            "Types.cs",
            self.generate_types(schema)?,
            |ns| format!("{}Client.cs", to_pascal_case(ns)),
            |ns| format!("{}Client", to_pascal_case(ns)),
            |ns, class_name, ns_schema| self.generate_namespace_client(ns, class_name, ns_schema),
        )?;
        let client_names: Vec<_> = ns_pairs.into_iter().map(|(_, name)| name).collect();
        output.add("Generated.cs", self.generate_index(&client_names));
        Ok(output)
    }
}

impl CSharpGenerator {
    fn generate_types(&self, schema: &Schema) -> Result<String> {
        let header = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            String::new(),
            "namespace Saikuro.Generated;".to_owned(),
            String::new(),
        ];

        generate_types_from_schema(
            schema,
            header,
            |type_name, fields| {
                let mut lines = vec![format!("public sealed record {type_name}(")];
                let mut field_lines = Vec::new();
                for (field_name, field_desc) in fields {
                    let cs_type = CSharpGenerator::type_to_cs(&field_desc.r#type)?;
                    let nullable = if field_desc.optional {
                        format!("{cs_type}?")
                    } else {
                        cs_type
                    };
                    let pascal = to_pascal_case(field_name);
                    field_lines.push(format!("    {nullable} {pascal}"));
                }
                lines.push(field_lines.join(",\n"));
                lines.push(");".to_owned());
                lines.push(String::new());
                Ok(lines)
            },
            |type_name, variants| {
                let mut lines = vec![format!("public enum {type_name} {{")];
                for variant in variants {
                    lines.push(format!("    {variant},"));
                }
                lines.push("}".to_owned());
                lines.push(String::new());
                Ok(lines)
            },
            |type_name, inner| {
                let cs_type = CSharpGenerator::type_to_cs(inner)?;
                Ok(vec![
                    format!("// {type_name} is an alias for {cs_type}"),
                    format!("using {type_name} = {cs_type};"),
                    String::new(),
                ])
            },
        )
    }

    fn generate_namespace_client(
        &self,
        ns_name: &str,
        class_name: &str,
        ns: &NamespaceSchema,
    ) -> Result<String> {
        let needs_stream = ns
            .functions
            .values()
            .any(|f| matches!(f.returns, TypeDescriptor::Stream { .. }));
        let needs_channel = ns
            .functions
            .values()
            .any(|f| matches!(f.returns, TypeDescriptor::Channel { .. }));

        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "".to_owned(),
            "using Saikuro;".to_owned(),
            "using Saikuro.Generated;".to_owned(),
        ];

        if needs_stream {
            lines.push("// SaikuroStream<T> is provided by the Saikuro adapter.".to_owned());
        }
        if needs_channel {
            lines.push(
                "// SaikuroChannel<TIn, TOut> is provided by the Saikuro adapter.".to_owned(),
            );
        }

        lines.push("".to_owned());
        lines.push("namespace Saikuro.Generated;".to_owned());
        lines.push("".to_owned());

        if let Some(doc) = &ns.doc {
            lines.push(format!("/// <summary>{doc}</summary>"));
        }

        lines.push(format!("public sealed class {class_name} {{"));
        lines.push("    private readonly SaikuroClient _client;".to_owned());
        lines.push("".to_owned());
        lines.push(format!(
            "    public {class_name}(SaikuroClient client) => _client = client;"
        ));
        lines.push("".to_owned());

        for (fn_name, fn_schema) in &ns.functions {
            if fn_schema.visibility == Visibility::Private {
                continue;
            }

            let method = self.generate_method(ns_name, fn_name, fn_schema)?;
            lines.push(method);
        }

        lines.push("}".to_owned());
        lines.push("".to_owned());

        Ok(lines.join("\n"))
    }

    fn generate_method(
        &self,
        ns_name: &str,
        fn_name: &str,
        schema: &FunctionSchema,
    ) -> Result<String> {
        let method_name = to_pascal_case(fn_name);
        let target = format!("{ns_name}.{fn_name}");

        let mut params: Vec<String> = vec![];
        let mut call_args_items: Vec<String> = vec![];

        for arg in &schema.args {
            let cs_type = Self::type_to_cs(&arg.r#type)?;
            if arg.optional {
                params.push(format!("{}? {} = null", cs_type, to_camel_case(&arg.name)));
            } else {
                params.push(format!("{} {}", cs_type, to_camel_case(&arg.name)));
            }
            call_args_items.push(to_camel_case(&arg.name));
        }

        let params_str = params.join(", ");
        let args_array = if call_args_items.is_empty() {
            "Array.Empty<object?>()".to_owned()
        } else {
            format!("new object?[] {{ {} }}", call_args_items.join(", "))
        };

        let mut lines = vec![];

        if let Some(doc) = &schema.doc {
            lines.push(format!("    /// <summary>{doc}</summary>"));
        }

        match &schema.returns {
            TypeDescriptor::Stream { item } => {
                let item_cs = Self::type_to_cs(item)?;
                lines.push(format!(
                    "    public Task<SaikuroStream<{item_cs}>> {method_name}Async({params_str}) =>"
                ));
                lines.push(format!(
                    "        _client.StreamAsync<{item_cs}>(\"{target}\","
                ));
                lines.push(format!("            {args_array});"));
            }
            TypeDescriptor::Channel { inbound, outbound } => {
                let in_cs = Self::type_to_cs(inbound)?;
                let out_cs = Self::type_to_cs(outbound)?;
                lines.push(format!(
                    "    public Task<SaikuroChannel<{in_cs}, {out_cs}>> {method_name}Async({params_str}) =>"
                ));
                lines.push(format!(
                    "        _client.ChannelAsync<{in_cs}, {out_cs}>(\"{target}\","
                ));
                lines.push(format!("            {args_array});"));
            }
            _ => {
                let return_cs = Self::type_to_cs(&schema.returns)?;
                if return_cs == "void" {
                    lines.push(format!(
                        "    public async Task {method_name}Async({params_str}) {{"
                    ));
                    lines.push(format!(
                        "        await _client.CallAsync(\"{target}\", {args_array});"
                    ));
                    lines.push("    }".to_owned());
                } else {
                    lines.push(format!(
                        "    public async Task<{return_cs}> {method_name}Async({params_str}) {{"
                    ));
                    lines.push(format!(
                        "        var result = await _client.CallAsync(\"{target}\", {args_array});"
                    ));
                    lines.push(format!("        return ({return_cs})result!;"));
                    lines.push("    }".to_owned());
                }
            }
        }

        lines.push("".to_owned());

        Ok(lines.join("\n"))
    }

    fn generate_index(&self, client_names: &[String]) -> String {
        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "//".to_owned(),
            "// Generated clients:".to_owned(),
        ];
        for name in client_names {
            lines.push(format!("//   {name}"));
        }
        lines.push("".to_owned());
        lines.join("\n")
    }

    fn type_to_cs(desc: &TypeDescriptor) -> Result<String> {
        Ok(match desc {
            TypeDescriptor::Primitive { r#type } => match r#type {
                PrimitiveType::Bool => "bool".to_owned(),
                PrimitiveType::I8 => "sbyte".to_owned(),
                PrimitiveType::I16 => "short".to_owned(),
                PrimitiveType::I32 => "int".to_owned(),
                PrimitiveType::I64 => "long".to_owned(),
                PrimitiveType::U8 => "byte".to_owned(),
                PrimitiveType::U16 => "ushort".to_owned(),
                PrimitiveType::U32 => "uint".to_owned(),
                PrimitiveType::U64 => "ulong".to_owned(),
                PrimitiveType::F32 => "float".to_owned(),
                PrimitiveType::F64 => "double".to_owned(),
                PrimitiveType::String => "string".to_owned(),
                PrimitiveType::Bytes => "byte[]".to_owned(),
                PrimitiveType::Any => "object?".to_owned(),
                PrimitiveType::Unit => "void".to_owned(),
            },
            TypeDescriptor::Named { name } => name.clone(),
            TypeDescriptor::Option { inner } => {
                let inner_cs = Self::type_to_cs(inner)?;
                format!("{inner_cs}?")
            }
            TypeDescriptor::Array { item } => {
                format!("List<{}>", Self::type_to_cs(item)?)
            }
            TypeDescriptor::Map { value } => {
                format!("Dictionary<string, {}>", Self::type_to_cs(value)?)
            }
            TypeDescriptor::Stream { item } => {
                format!("SaikuroStream<{}>", Self::type_to_cs(item)?)
            }
            TypeDescriptor::Channel { inbound, outbound } => {
                format!(
                    "SaikuroChannel<{}, {}>",
                    Self::type_to_cs(inbound)?,
                    Self::type_to_cs(outbound)?
                )
            }
        })
    }
}
