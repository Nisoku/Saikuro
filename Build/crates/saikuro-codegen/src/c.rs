//! C binding generator.
//!
//! Produces:
//! - A `saikuro_types.h` placeholder for named schema types.
//! - One `<namespace>_client.h` per namespace with schema-aware C helper calls.
//! - A `saikuro_generated.h` umbrella include.

use saikuro_core::schema::{NamespaceSchema, Schema, Visibility};

use crate::{
    error::Result,
    generator::{BindingGenerator, GeneratorOutput},
};

pub struct CGenerator;

impl BindingGenerator for CGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();

        output.add("saikuro_types.h", self.generate_types(schema));

        let mut includes = vec![
            "#ifndef SAIKURO_GENERATED_H".to_owned(),
            "#define SAIKURO_GENERATED_H".to_owned(),
            String::new(),
            "#include \"saikuro.h\"".to_owned(),
            "#include \"saikuro_types.h\"".to_owned(),
        ];

        for (ns_name, ns_schema) in &schema.namespaces {
            let file_name = format!("{ns_name}_client.h");
            let src = self.generate_namespace_client(ns_name, ns_schema);
            output.add(&file_name, src);
            includes.push(format!("#include \"{file_name}\""));
        }

        includes.push(String::new());
        includes.push("#endif".to_owned());
        includes.push(String::new());

        output.add("saikuro_generated.h", includes.join("\n"));

        Ok(output)
    }
}

impl CGenerator {
    fn generate_types(&self, _schema: &Schema) -> String {
        vec![
            "#ifndef SAIKURO_TYPES_H".to_owned(),
            "#define SAIKURO_TYPES_H".to_owned(),
            String::new(),
            "/* AUTO-GENERATED by saikuro-codegen. DO NOT EDIT. */".to_owned(),
            "/*".to_owned(),
            " * Named schema types are currently represented as JSON strings in the C adapter."
                .to_owned(),
            " * This header is reserved for future generated struct/enum declarations.".to_owned(),
            " */".to_owned(),
            String::new(),
            "#endif".to_owned(),
            String::new(),
        ]
        .join("\n")
    }

    fn generate_namespace_client(&self, ns_name: &str, ns: &NamespaceSchema) -> String {
        let guard = format!("SAIKURO_{}_CLIENT_H", ns_name.to_uppercase());

        let mut lines = vec![
            format!("#ifndef {guard}"),
            format!("#define {guard}"),
            String::new(),
            "/* AUTO-GENERATED by saikuro-codegen. DO NOT EDIT. */".to_owned(),
            "#include \"saikuro.h\"".to_owned(),
            String::new(),
        ];

        for (fn_name, fn_schema) in &ns.functions {
            if fn_schema.visibility == Visibility::Private {
                continue;
            }

            let c_fn_name = format!("{}_{}", sanitize_ident(ns_name), sanitize_ident(fn_name));
            let target = format!("{ns_name}.{fn_name}");
            if let Some(doc) = &fn_schema.doc {
                lines.push(format!("/* {doc} */"));
            }
            lines.push(format!(
                "static inline char* {c_fn_name}(saikuro_client_t client, const char* args_json) {{"
            ));
            lines.push(format!(
                "    return saikuro_client_call_json(client, \"{target}\", args_json);"
            ));
            lines.push("}".to_owned());
            lines.push(String::new());
        }

        lines.push("#endif".to_owned());
        lines.push(String::new());

        lines.join("\n")
    }
}

fn sanitize_ident(s: &str) -> String {
    s.chars()
        .map(|c| if c.is_ascii_alphanumeric() { c } else { '_' })
        .collect()
}
