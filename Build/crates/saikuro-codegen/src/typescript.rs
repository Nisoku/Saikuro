//! TypeScript binding generator.
//!
//! Produces:
//! - A `types.ts` with interfaces for all named types.
//! - One `<namespace>Client.ts` per namespace with a typed async client class.
//! - An `index.ts` that re-exports everything.

use saikuro_core::schema::{
    FunctionSchema, NamespaceSchema, PrimitiveType, Schema, TypeDescriptor,
};

use crate::{
    error::Result,
    generator::{
        convert_type, generate_types_and_namespace_clients, generate_types_from_schema,
        BindingGenerator, GeneratorOutput, TypeConverter,
    },
    to_pascal_case,
};

pub struct TypeScriptGenerator;

impl BindingGenerator for TypeScriptGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();
        let ns_pairs = generate_types_and_namespace_clients(
            schema,
            &mut output,
            "types.ts",
            self.generate_types(schema)?,
            |ns| format!("{}Client.ts", to_pascal_case(ns)),
            |ns| format!("{}Client", to_pascal_case(ns)),
            |ns, class_name, ns_schema| self.generate_namespace_client(ns, class_name, ns_schema),
        )?;
        let mut index_exports = vec!["export * from './types';".to_owned()];
        for (_, class_name) in &ns_pairs {
            index_exports.push(format!(
                "export {{ {class_name} }} from './{class_name}';\n"
            ));
        }
        output.add("index.ts", index_exports.join("\n") + "\n");
        Ok(output)
    }
}

impl TypeScriptGenerator {
    fn generate_types(&self, schema: &Schema) -> Result<String> {
        let header = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            String::new(),
        ];

        let type_to_ts = |desc: &TypeDescriptor| self.type_to_ts(desc);

        generate_types_from_schema(
            schema,
            header,
            |type_name, fields| {
                let mut lines = vec![format!("export interface {type_name} {{")];
                for (field_name, field_desc) in fields {
                    let ts_type = type_to_ts(&field_desc.r#type);
                    let optional = if field_desc.optional { "?" } else { "" };
                    lines.push(format!("  {field_name}{optional}: {ts_type};"));
                }
                lines.push("}".to_owned());
                lines.push(String::new());
                Ok(lines)
            },
            |type_name, variants| {
                let mut lines = vec![format!("export type {type_name} =")];
                let variants_str: Vec<String> =
                    variants.iter().map(|v| format!("  | \"{v}\"")).collect();
                lines.push(variants_str.join("\n") + ";");
                lines.push(String::new());
                Ok(lines)
            },
            |type_name, inner| {
                let ts_type = type_to_ts(inner);
                Ok(vec![
                    format!("export type {type_name} = {ts_type};"),
                    String::new(),
                ])
            },
        )
    }

    fn generate_namespace_client(
        &self,
        ns_name: &str,
        class_name: &str,
        ns: &NamespaceSchema,
    ) -> Result<String> {
        let needs_channel = ns
            .functions
            .values()
            .any(|f| matches!(f.returns, TypeDescriptor::Channel { .. }));

        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "import type { SaikuroClient } from 'saikuro';".to_owned(),
        ];

        if needs_channel {
            lines.push("import type { SaikuroChannel } from 'saikuro';".to_owned());
        }

        lines.push("import type * as Types from './types';".to_owned());
        lines.push(String::new());
        lines.push(format!("export class {class_name} {{"));
        lines.push("  constructor(private readonly client: SaikuroClient) {}".to_owned());
        lines.push(String::new());

        for (fn_name, fn_schema) in crate::generator::namespace_public_functions(ns) {
            let method = self.generate_method(ns_name, fn_name, fn_schema)?;
            lines.push(method);
        }

        lines.push("}".to_owned());
        lines.push(String::new());

        Ok(lines.join("\n"))
    }

    fn generate_method(
        &self,
        ns_name: &str,
        fn_name: &str,
        schema: &FunctionSchema,
    ) -> Result<String> {
        let mut params = vec![];
        let mut call_args = vec![];

        for arg in &schema.args {
            let ts_type = self.type_to_ts(&arg.r#type);
            let optional = if arg.optional { "?" } else { "" };
            params.push(format!("{}{}: {}", arg.name, optional, ts_type));
            call_args.push(arg.name.clone());
        }

        let target = format!("{ns_name}.{fn_name}");
        let args_list = call_args.join(", ");

        let mut lines = vec![];

        if let Some(doc) = &schema.doc {
            lines.push(format!("  /** {doc} */"));
        }

        // Choose the correct client primitive based on the return type.
        match &schema.returns {
            TypeDescriptor::Stream { item } => {
                let item_ts = self.type_to_ts(item);
                lines.push(format!(
                    "  async {}({}): Promise<AsyncIterable<{}>> {{",
                    fn_name,
                    params.join(", "),
                    item_ts
                ));
                lines.push(format!(
                    "    return this.client.stream<{item_ts}>('{target}', [{args_list}]);"
                ));
                lines.push("  }".to_owned());
            }
            TypeDescriptor::Channel { inbound, outbound } => {
                let in_ts = self.type_to_ts(inbound);
                let out_ts = self.type_to_ts(outbound);
                lines.push(format!(
                    "  async {}({}): Promise<SaikuroChannel<{}, {}>> {{",
                    fn_name,
                    params.join(", "),
                    in_ts,
                    out_ts,
                ));
                lines.push(format!(
                    "    return this.client.channel<{in_ts}, {out_ts}>('{target}', [{args_list}]);"
                ));
                lines.push("  }".to_owned());
            }
            _ => {
                let return_type = self.type_to_ts(&schema.returns);
                lines.push(format!(
                    "  async {}({}): Promise<{}> {{",
                    fn_name,
                    params.join(", "),
                    return_type
                ));
                lines.push(format!(
                    "    return this.client.call('{target}', [{args_list}]) as Promise<{return_type}>;"
                ));
                lines.push("  }".to_owned());
            }
        }

        lines.push(String::new());

        Ok(lines.join("\n"))
    }

    fn type_to_ts(&self, desc: &TypeDescriptor) -> String {
        convert_type(desc, self)
    }
}

impl TypeConverter for TypeScriptGenerator {
    fn primitive_name(&self, t: &PrimitiveType) -> &'static str {
        match t {
            PrimitiveType::Bool => "boolean",
            PrimitiveType::I8
            | PrimitiveType::I16
            | PrimitiveType::I32
            | PrimitiveType::I64
            | PrimitiveType::U8
            | PrimitiveType::U16
            | PrimitiveType::U32
            | PrimitiveType::U64
            | PrimitiveType::F32
            | PrimitiveType::F64 => "number",
            PrimitiveType::String => "string",
            PrimitiveType::Bytes => "Uint8Array",
            PrimitiveType::Any => "unknown",
            PrimitiveType::Unit => "void",
        }
    }

    fn named_type(&self, name: &str) -> String {
        format!("Types.{name}")
    }

    fn wrap_option(&self, inner: &str) -> String {
        format!("{inner} | null")
    }

    fn wrap_array(&self, inner: &str) -> String {
        format!("Array<{inner}>")
    }

    fn wrap_map(&self, value: &str) -> String {
        format!("Record<string, {value}>")
    }

    fn wrap_stream(&self, item: &str) -> String {
        format!("AsyncIterable<{item}>")
    }

    fn wrap_channel(&self, inbound: &str, outbound: &str) -> String {
        format!("SaikuroChannel<{inbound}, {outbound}>")
    }
}
