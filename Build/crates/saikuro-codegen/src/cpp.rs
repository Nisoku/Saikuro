fn sanitize_ident(s: &str) -> String {
    let mut result: String = s
        .chars()
        .map(|c| if c.is_ascii_alphanumeric() { c } else { '_' })
        .collect();

    while result.contains("__") {
        result = result.replace("__", "_");
    }

    while result.starts_with('_') {
        result.remove(0);
    }

    if result
        .chars()
        .next()
        .map(|c| c.is_ascii_digit())
        .unwrap_or(false)
    {
        result.insert(0, '_');
    }

    if result.is_empty() {
        result.push('s');
    }

    while result.contains("__") {
        result = result.replace("__", "_");
    }

    if CPP_RESERVED.contains(&result.as_str()) {
        result.push('_');
    }
    result
}

fn escape_cpp_string_literal(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 8);
    for c in s.chars() {
        match c {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\t' => out.push_str("\\t"),
            '\r' => out.push_str("\\r"),
            '\x08' => out.push_str("\\b"),
            '\x0C' => out.push_str("\\f"),
            '\0' => out.push_str("\\0"),
            '\x0B' => out.push_str("\\v"),
            c if c.is_control() || (c as u32) < 0x20 || (c as u32) == 0x7F => {
                use std::fmt::Write;
                let code = c as u32;
                if code <= 0xFF {
                    write!(out, "\\x{:02X}", code).unwrap();
                } else if code <= 0xFFFF {
                    write!(out, "\\u{:04X}", code).unwrap();
                } else {
                    write!(out, "\\U{:08X}", code).unwrap();
                }
            }
            _ => out.push(c),
        }
    }
    out
}
