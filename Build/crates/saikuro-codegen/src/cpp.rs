//! C++ binding generator.
//!
//! Produces:
//! - One `<Namespace>Client.hpp` per namespace with schema-aware wrappers.
//! - A `saikuro_generated.hpp` umbrella include.

use saikuro_core::schema::{NamespaceSchema, Schema, Visibility};

use crate::{
    error::Result,
    generator::{BindingGenerator, GeneratorOutput},
};

pub struct CppGenerator;

impl BindingGenerator for CppGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();

        let mut includes = vec![
            "#pragma once".to_owned(),
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "#include <saikuro/saikuro.hpp>".to_owned(),
        ];

        for (ns_name, ns_schema) in &schema.namespaces {
            let class_name = format!("{}Client", to_pascal_case(ns_name));
            let file_name = format!("{class_name}.hpp");
            let src = self.generate_namespace_client(ns_name, &class_name, ns_schema);
            output.add(&file_name, src);
            includes.push(format!("#include \"{file_name}\""));
        }

        includes.push(String::new());
        output.add("saikuro_generated.hpp", includes.join("\n"));

        Ok(output)
    }
}

impl CppGenerator {
    fn generate_namespace_client(
        &self,
        ns_name: &str,
        class_name: &str,
        ns: &NamespaceSchema,
    ) -> String {
        let mut lines = vec![
            "#pragma once".to_owned(),
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "#include <string>".to_owned(),
            "#include <saikuro/saikuro.hpp>".to_owned(),
            String::new(),
            "namespace saikuro::generated {".to_owned(),
            format!("class {class_name} {{"),
            "public:".to_owned(),
            [
                "    explicit ",
                class_name,
                "(const saikuro::Client& client) : client_(client) {}"
            ].join("") ,
            String::new(),
        ];

        for (fn_name, fn_schema) in &ns.functions {
            if fn_schema.visibility == Visibility::Private {
                continue;
            }

            let method_name = sanitize_ident(fn_name);
            let target = format!("{ns_name}.{fn_name}");
            if let Some(doc) = &fn_schema.doc {
                for line in doc.lines() {
                    lines.push(format!("    // {}", line.trim_end()));
                }
            }
            lines.push(format!(
                "    std::string {method_name}(const std::string& args_json) const {{"
            ));
            lines.push(format!(
                "        return client_.call_json(\"{target}\", args_json);"
            ));
            lines.push("    }".to_owned());
            lines.push(String::new());
        }

        lines.push("private:".to_owned());
        lines.push("    const saikuro::Client& client_;".to_owned());
        lines.push("};".to_owned());
        lines.push("}  // namespace saikuro::generated".to_owned());
        lines.push(String::new());

        lines.join("\n")
    }
}

fn to_pascal_case(s: &str) -> String {
    s.split('_')
        .map(|part| {
            let mut c = part.chars();
            match c.next() {
                None => String::new(),
                Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
            }
        })
        .collect()
}

fn sanitize_ident(s: &str) -> String {
    let mut result: String = s
        .chars()
        .map(|c| if c.is_ascii_alphanumeric() { c } else { '_' })
        .collect();
    if result
        .chars()
        .next()
        .map(|c| c.is_ascii_digit())
        .unwrap_or(false)
    {
        result.insert(0, '_');
    }
    if result.is_empty() {
        result.push('_');
    }
    result
}
