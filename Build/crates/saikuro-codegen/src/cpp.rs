//! C++ binding generator.
//!
//! Produces:
//! - One `<Namespace>Client.hpp` per namespace with schema-aware wrappers.
//! - A `saikuro_generated.hpp` umbrella include.

use saikuro_core::schema::{NamespaceSchema, Schema, Visibility};
use std::collections::{HashMap, HashSet};

use crate::{
    error::{CodegenError, Result},
    generator::{BindingGenerator, GeneratorOutput},
};

pub struct CppGenerator;

impl BindingGenerator for CppGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();

        let mut includes = vec![
            "#pragma once".to_owned(),
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "#include <saikuro/saikuro.hpp>".to_owned(),
        ];

        let mut ns_items: Vec<_> = schema.namespaces.iter().collect();
        ns_items.sort_by_key(|(k, _)| *k);
        let mut class_names: HashMap<String, String> = HashMap::new();
        let mut file_names: HashMap<String, String> = HashMap::new();
        for (ns_name, ns_schema) in ns_items {
            let class_name = sanitize_ident(&format!("{}Client", to_pascal_case(ns_name)));
            if let Some(previous_ns) = class_names.get(&class_name) {
                return Err(CodegenError::Schema(format!(
                    "C++ class name collision: namespaces '{previous_ns}' and '{ns_name}' both sanitize to '{class_name}'"
                )));
            }
            class_names.insert(class_name.clone(), ns_name.clone());

            let file_name = format!("{class_name}.hpp");
            if let Some(previous_ns) = file_names.get(&file_name) {
                return Err(CodegenError::Schema(format!(
                    "C++ file name collision: namespaces '{previous_ns}' and '{ns_name}' both map to '{file_name}'"
                )));
            }
            file_names.insert(file_name.clone(), ns_name.clone());

            let src = self.generate_namespace_client(ns_name, &class_name, ns_schema)?;
            output.add(&file_name, src);
            includes.push(format!("#include \"{file_name}\""));
        }

        includes.push(String::new());
        output.add("saikuro_generated.hpp", includes.join("\n"));

        Ok(output)
    }
}

impl CppGenerator {
    fn generate_namespace_client(
        &self,
        ns_name: &str,
        class_name: &str,
        ns: &NamespaceSchema,
    ) -> Result<String> {
        let mut lines = vec![
            "#pragma once".to_owned(),
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "#include <string>".to_owned(),
            "#include <saikuro/saikuro.hpp>".to_owned(),
            String::new(),
            "namespace saikuro::generated {".to_owned(),
            format!("class {class_name} {{"),
            "public:".to_owned(),
            [
                "    explicit ",
                class_name,
                "(const saikuro::Client& client) : client_(client) {}",
            ]
            .join(""),
            String::new(),
        ];

        let mut fn_items: Vec<_> = ns.functions.iter().collect();
        fn_items.sort_by_key(|(k, _)| *k);
        let mut seen_methods: HashSet<String> = HashSet::new();
        seen_methods.insert(sanitize_ident(class_name));
        seen_methods.insert("client_".to_owned());
        for (fn_name, fn_schema) in fn_items {
            if fn_schema.visibility == Visibility::Private {
                continue;
            }

            let method_name = sanitize_ident(fn_name);
            if !seen_methods.insert(method_name.clone()) {
                return Err(CodegenError::Schema(format!(
                    "C++ method name collision in namespace '{ns_name}': function '{fn_name}' maps to duplicate method '{method_name}'"
                )));
            }

            let target = escape_cpp_string_literal(&format!("{ns_name}.{fn_name}"));
            if let Some(doc) = &fn_schema.doc {
                for line in doc.lines() {
                    lines.push(format!("    // {}", line.trim_end()));
                }
            }
            lines.push(format!(
                "    std::string {method_name}(const std::string& args_json) const {{"
            ));
            lines.push(format!(
                "        return client_.call_json(\"{target}\", args_json);"
            ));
            lines.push("    }".to_owned());
            lines.push(String::new());
        }

        lines.push("private:".to_owned());
        lines.push("    const saikuro::Client& client_;".to_owned());
        lines.push("};".to_owned());
        lines.push("}  // namespace saikuro::generated".to_owned());
        lines.push(String::new());

        Ok(lines.join("\n"))
    }
}

fn to_pascal_case(s: &str) -> String {
    s.split('_')
        .map(|part| {
            let mut c = part.chars();
            match c.next() {
                None => String::new(),
                Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
            }
        })
        .collect()
}

