//! Rust binding generator.
//!
//! Produces:
//! - A `types.rs` with Rust types for all named schema types.
//! - One `<namespace>_client.rs` per namespace with typed async wrappers over `saikuro::Client`.
//! - A `mod.rs` that re-exports generated modules.

use saikuro_core::schema::{
    FunctionSchema, NamespaceSchema, PrimitiveType, Schema, TypeDefinition, TypeDescriptor,
    Visibility,
};

use crate::{
    error::Result,
    generator::{BindingGenerator, GeneratorOutput},
};

pub struct RustGenerator;

impl BindingGenerator for RustGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();

        output.add("types.rs", self.generate_types(schema)?);

        let mut mods = vec!["pub mod types;".to_owned(), "pub use types::*;".to_owned()];

        for (ns_name, ns_schema) in &schema.namespaces {
            let module_name = format!("{}_client", sanitize_ident(ns_name));
            let file_name = format!("{module_name}.rs");
            let class_name = format!("{}Client", sanitize_then_pascal(ns_name));
            output.add(
                &file_name,
                self.generate_namespace_client(ns_name, &class_name, ns_schema)?,
            );
            mods.push(format!("pub mod {module_name};"));
            mods.push(format!("pub use {module_name}::{class_name};"));
        }

        mods.push(String::new());
        output.add("mod.rs", mods.join("\n"));

        Ok(output)
    }
}

impl RustGenerator {
    fn generate_types(&self, schema: &Schema) -> Result<String> {
        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "".to_owned(),
            "use serde::{Deserialize, Serialize};".to_owned(),
            "".to_owned(),
        ];

        for (type_name, type_def) in &schema.types {
            let safe_type_name = sanitize_type_name(type_name);
            match type_def {
                TypeDefinition::Record { fields } => {
                    lines.push("#[derive(Debug, Clone, Serialize, Deserialize)]".to_owned());
                    lines.push(format!("pub struct {safe_type_name} {{"));
                    for (field_name, field_desc) in fields {
                        let rust_type = Self::type_to_rust(&field_desc.r#type)?;
                        let final_type = if field_desc.optional {
                            format!("Option<{rust_type}>")
                        } else {
                            rust_type
                        };
                        lines.push(format!(
                            "    pub {}: {},",
                            sanitize_ident(field_name),
                            final_type
                        ));
                    }
                    lines.push("}".to_owned());
                    lines.push("".to_owned());
                }
                TypeDefinition::Enum { variants } => {
                    lines.push("#[derive(Debug, Clone, Serialize, Deserialize)]".to_owned());
                    lines.push(format!("pub enum {safe_type_name} {{"));
                    for variant in variants {
                        lines.push(format!(
                            "    {},",
                            sanitize_variant_preserve_leading(variant)
                        ));
                    }
                    lines.push("}".to_owned());
                    lines.push("".to_owned());
                }
                TypeDefinition::Alias { inner } => {
                    lines.push(format!(
                        "pub type {safe_type_name} = {};",
                        Self::type_to_rust(inner)?
                    ));
                    lines.push("".to_owned());
                }
            }
        }

        Ok(lines.join("\n"))
    }

    fn generate_namespace_client(
        &self,
        ns_name: &str,
        class_name: &str,
        ns: &NamespaceSchema,
    ) -> Result<String> {
        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "".to_owned(),
            "use super::types::*;".to_owned(),
            "use saikuro::{Client, Error, Result};".to_owned(),
            "".to_owned(),
            format!("pub struct {class_name}<'a> {{"),
            "    client: &'a Client,".to_owned(),
            "}".to_owned(),
            "".to_owned(),
            format!("impl<'a> {class_name}<'a> {{"),
            "    pub fn new(client: &'a Client) -> Self {".to_owned(),
            "        Self { client }".to_owned(),
            "    }".to_owned(),
            "".to_owned(),
        ];

        for (fn_name, fn_schema) in &ns.functions {
            if fn_schema.visibility == Visibility::Private {
                continue;
            }
            lines.push(self.generate_method(ns_name, fn_name, fn_schema)?);
        }

        lines.push("}".to_owned());
        lines.push("".to_owned());

        Ok(lines.join("\n"))
    }

    fn generate_method(
        &self,
        ns_name: &str,
        fn_name: &str,
        schema: &FunctionSchema,
    ) -> Result<String> {
        let target = format!("{ns_name}.{fn_name}");
        let method_name = sanitize_ident(fn_name);

        let mut params = vec![];
        let mut arg_pushes = vec![];

        for arg in &schema.args {
            let arg_name = sanitize_ident(&arg.name);
            let arg_type = Self::type_to_rust(&arg.r#type)?;
            let final_type = if arg.optional {
                format!("Option<{arg_type}>")
            } else {
                arg_type
            };
            params.push(format!("{arg_name}: {final_type}"));
            arg_pushes.push(format!(
                "        args.push(serde_json::to_value({arg_name}).map_err(|e| Error::Codec(e.to_string()))?);"
            ));
        }

        let mut lines = vec![];
        if let Some(doc) = &schema.doc {
            for line in doc.lines() {
                lines.push(format!("    /// {line}"));
            }
        }

        match &schema.returns {
            TypeDescriptor::Stream { .. } => {
                lines.push(format!(
                    "    pub async fn {method_name}(&self, {}) -> Result<saikuro::SaikuroStream> {{",
                    params.join(", ")
                ));
                lines.push("        let mut args = Vec::new();".to_owned());
                lines.extend(arg_pushes);
                lines.push(format!(
                    "        self.client.stream(\"{target}\", args).await"
                ));
                lines.push("    }".to_owned());
            }
            TypeDescriptor::Channel { .. } => {
                lines.push(format!(
                    "    pub async fn {method_name}(&self, {}) -> Result<saikuro::SaikuroChannel> {{",
                    params.join(", ")
                ));
                lines.push("        let mut args = Vec::new();".to_owned());
                lines.extend(arg_pushes);
                lines.push(format!(
                    "        self.client.channel(\"{target}\", args).await"
                ));
                lines.push("    }".to_owned());
            }
            _ => {
                let return_type = Self::type_to_rust(&schema.returns)?;
                lines.push(format!(
                    "    pub async fn {method_name}(&self, {}) -> Result<{return_type}> {{",
                    params.join(", ")
                ));
                lines.push("        let mut args = Vec::new();".to_owned());
                lines.extend(arg_pushes);
                lines.push(format!(
                    "        let value = self.client.call(\"{target}\", args).await?;"
                ));
                lines.push(format!(
                    "        serde_json::from_value::<{return_type}>(value).map_err(|e| Error::Codec(e.to_string()))"
                ));
                lines.push("    }".to_owned());
            }
        }

        lines.push("".to_owned());
        Ok(lines.join("\n"))
    }

    fn type_to_rust(desc: &TypeDescriptor) -> Result<String> {
        Ok(match desc {
            TypeDescriptor::Primitive { r#type } => match r#type {
                PrimitiveType::Bool => "bool".to_owned(),
                PrimitiveType::I8 => "i8".to_owned(),
                PrimitiveType::I16 => "i16".to_owned(),
                PrimitiveType::I32 => "i32".to_owned(),
                PrimitiveType::I64 => "i64".to_owned(),
                PrimitiveType::U8 => "u8".to_owned(),
                PrimitiveType::U16 => "u16".to_owned(),
                PrimitiveType::U32 => "u32".to_owned(),
                PrimitiveType::U64 => "u64".to_owned(),
                PrimitiveType::F32 => "f32".to_owned(),
                PrimitiveType::F64 => "f64".to_owned(),
                PrimitiveType::String => "String".to_owned(),
                PrimitiveType::Bytes => "Vec<u8>".to_owned(),
                PrimitiveType::Any => "serde_json::Value".to_owned(),
                PrimitiveType::Unit => "()".to_owned(),
            },
            TypeDescriptor::Named { name } => sanitize_type_name(name),
            TypeDescriptor::Option { inner } => format!("Option<{}>", Self::type_to_rust(inner)?),
            TypeDescriptor::Array { item } => format!("Vec<{}>", Self::type_to_rust(item)?),
            TypeDescriptor::Map { value } => {
                format!(
                    "std::collections::HashMap<String, {}>",
                    Self::type_to_rust(value)?
                )
            }
            TypeDescriptor::Stream { item } => {
                format!(
                    "saikuro::SaikuroStream /* item: {} */",
                    Self::type_to_rust(item)?
                )
            }
            TypeDescriptor::Channel { inbound, outbound } => format!(
                "saikuro::SaikuroChannel /* in: {}, out: {} */",
                Self::type_to_rust(inbound)?,
                Self::type_to_rust(outbound)?
            ),
        })
    }
}

fn sanitize_ident(s: &str) -> String {
    let mut out: String = s
        .chars()
        .map(|c| if c.is_ascii_alphanumeric() { c } else { '_' })
        .collect();
    if out.is_empty() {
        out.push('_');
    }
    if out.chars().next().is_some_and(|c| c.is_ascii_digit()) {
        out.insert(0, '_');
    }
    out
}

fn sanitize_variant_preserve_leading(s: &str) -> String {
    let ident = sanitize_ident(s);
    let leading = ident.starts_with('_');
    let body = to_pascal_case(ident.trim_start_matches('_'));
    if leading {
        format!("_{}", body)
    } else {
        body
    }
}

fn sanitize_then_pascal(s: &str) -> String {
    let ident = sanitize_ident(s);
    let leading = ident.starts_with('_');
    let body = to_pascal_case(ident.trim_start_matches('_'));
    if leading {
        format!("_{}", body)
    } else {
        body
    }
}

fn sanitize_type_name(s: &str) -> String {
    let ident = sanitize_ident(s);
    if ident.is_empty() {
        "_".to_string()
    } else if ident.chars().next().unwrap().is_ascii_digit() {
        format!("_{}", ident)
    } else {
        ident
    }
}

fn to_pascal_case(s: &str) -> String {
    s.split('_')
        .map(|part| {
            let mut c = part.chars();
            match c.next() {
                None => String::new(),
                Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
            }
        })
        .collect()
}
