//! Rust binding generator.
//!
//! Produces:
//! - A `types.rs` with Rust types for all named schema types.
//! - One `<namespace>_client.rs` per namespace with typed async wrappers over `saikuro::Client`.
//! - A `mod.rs` that re-exports generated modules.

use saikuro_core::schema::{
    FunctionSchema, NamespaceSchema, PrimitiveType, Schema, TypeDefinition, TypeDescriptor,
};

use crate::{
    error::{CodegenError, Result},
    generator::{convert_type, BindingGenerator, GeneratorOutput, TypeConverter},
    to_pascal_case,
};

use std::collections::HashMap;

pub struct RustGenerator;

impl BindingGenerator for RustGenerator {
    fn generate(&self, schema: &Schema) -> Result<GeneratorOutput> {
        let mut output = GeneratorOutput::default();

        output.add("types.rs", self.generate_types(schema)?);

        let mut mods = vec!["pub mod types;".to_owned(), "pub use types::*;".to_owned()];
        let mut module_names = HashMap::new();
        let mut file_names = HashMap::new();
        let mut class_names = HashMap::new();

        let mut ns_keys: Vec<_> = schema.namespaces.keys().cloned().collect();
        ns_keys.sort();
        for ns_name in ns_keys {
            let ns_schema = &schema.namespaces[&ns_name];
            let module_name_base = format!("{}_client", sanitize_ident(&ns_name));
            let module_name =
                ensure_unique_name("module", &ns_name, &module_name_base, &mut module_names)?;
            let file_name_base = format!("{module_name}.rs");
            let file_name = ensure_unique_name("file", &ns_name, &file_name_base, &mut file_names)?;
            let class_name_base = format!("{}Client", sanitize_pascal_preserve_leading(&ns_name));
            let class_name = ensure_unique_name(
                "namespace client type",
                &ns_name,
                &class_name_base,
                &mut class_names,
            )?;
            output.add(
                &file_name,
                self.generate_namespace_client(&ns_name, &class_name, ns_schema)?,
            );
            mods.push(format!("pub mod {module_name};"));
            mods.push(format!("pub use {module_name}::{class_name};"));
        }

        mods.push(String::new());
        output.add("mod.rs", mods.join("\n"));

        Ok(output)
    }
}

impl RustGenerator {
    fn generate_types(&self, schema: &Schema) -> Result<String> {
        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "".to_owned(),
        ];

        let mut type_keys: Vec<_> = schema.types.keys().cloned().collect();
        type_keys.sort();
        let mut type_names = HashMap::new();
        for type_name in type_keys {
            let type_def = &schema.types[&type_name];
            let safe_type_name = ensure_unique_name(
                "type",
                &type_name,
                &sanitize_type_name_pascal(&type_name),
                &mut type_names,
            )?;
            match type_def {
                TypeDefinition::Record { fields } => {
                    lines.push(
                        "#[derive(Debug, Clone, ::serde::Serialize, ::serde::Deserialize)]"
                            .to_owned(),
                    );
                    lines.push(format!("pub struct {safe_type_name} {{"));
                    let mut field_names = HashMap::new();
                    let mut field_keys: Vec<_> = fields.keys().cloned().collect();
                    field_keys.sort();
                    for field_name in field_keys {
                        let field_desc = &fields[&field_name];
                        let rust_type = Self::type_to_rust(&field_desc.r#type);
                        let final_type = if field_desc.optional {
                            format!("Option<{rust_type}>")
                        } else {
                            rust_type
                        };
                        let safe_field_name = ensure_unique_name(
                            &format!("field in type {safe_type_name}"),
                            &field_name,
                            &sanitize_ident(&field_name),
                            &mut field_names,
                        )?;
                        // Preserve original wire name via serde rename
                        if field_desc.optional {
                            lines.push(format!(
                                "    #[serde(rename = {:?}, default, skip_serializing_if = \"Option::is_none\")]",
                                field_name
                            ));
                        } else {
                            lines.push(format!("    #[serde(rename = {:?})]", field_name));
                        }
                        lines.push(format!("    pub {safe_field_name}: {final_type},"));
                    }
                    lines.push("}".to_owned());
                    lines.push("".to_owned());
                }
                TypeDefinition::Enum { variants } => {
                    lines.push(
                        "#[derive(Debug, Clone, ::serde::Serialize, ::serde::Deserialize)]"
                            .to_owned(),
                    );
                    lines.push(format!("pub enum {safe_type_name} {{"));
                    let mut variant_names = HashMap::new();
                    for variant in variants {
                        let safe_variant = ensure_unique_name(
                            &format!("variant in enum {safe_type_name}"),
                            variant,
                            &sanitize_pascal_preserve_leading(variant),
                            &mut variant_names,
                        )?;
                        // Preserve original variant wire name via serde rename
                        lines.push(format!("    #[serde(rename = {:?})]", variant));
                        lines.push(format!("    {safe_variant},"));
                    }
                    lines.push("}".to_owned());
                    lines.push("".to_owned());
                }
                TypeDefinition::Alias { inner } => {
                    lines.push(format!(
                        "pub type {safe_type_name} = {};",
                        Self::type_to_rust(inner)
                    ));
                    lines.push("".to_owned());
                }
            }
        }

        Ok(lines.join("\n"))
    }

    fn generate_namespace_client(
        &self,
        ns_name: &str,
        class_name: &str,
        ns: &NamespaceSchema,
    ) -> Result<String> {
        let mut lines = vec![
            "// AUTO-GENERATED by saikuro-codegen. DO NOT EDIT.".to_owned(),
            "".to_owned(),
            "use saikuro::{Client, Error, Result};".to_owned(),
            "".to_owned(),
            format!("pub struct {class_name}<'a> {{"),
            "    client: &'a Client,".to_owned(),
            "}".to_owned(),
            "".to_owned(),
            format!("impl<'a> {class_name}<'a> {{"),
            "    pub fn new(client: &'a Client) -> Self {".to_owned(),
            "        Self { client }".to_owned(),
            "    }".to_owned(),
            "".to_owned(),
        ];

        let mut method_names = HashMap::new();
        for (fn_name, fn_schema) in crate::generator::namespace_public_functions(ns) {
            let method_name = ensure_unique_name(
                &format!("method in namespace {ns_name}"),
                fn_name,
                &sanitize_ident(fn_name),
                &mut method_names,
            )?;
            lines.push(self.generate_method(ns_name, fn_name, &method_name, fn_schema)?);
        }

        lines.push("}".to_owned());
        lines.push("".to_owned());

        Ok(lines.join("\n"))
    }

    fn generate_method(
        &self,
        ns_name: &str,
        fn_name: &str,
        method_name: &str,
        schema: &FunctionSchema,
    ) -> Result<String> {
        let target = format!("{ns_name}.{fn_name}");

        let mut params = vec![];
        let mut arg_pushes = vec![];
        let mut arg_names = HashMap::new();

        for arg in &schema.args {
            let arg_name = ensure_unique_name(
                &format!("argument in method {method_name}"),
                &arg.name,
                &sanitize_ident(&arg.name),
                &mut arg_names,
            )?;
            let arg_type = Self::type_to_rust_in_client(&arg.r#type);
            let final_type = if arg.optional {
                format!("Option<{arg_type}>")
            } else {
                arg_type
            };
            params.push(format!("{arg_name}: {final_type}"));
            if arg.optional {
                arg_pushes.push(format!(
                    "        if let Some(value) = {arg_name} {{ args.push(serde_json::to_value(value).map_err(|e| Error::Codec(e.to_string()))?); }} else {{ args.push(serde_json::Value::Null); }}"
                ));
            } else {
                arg_pushes.push(format!(
                    "        args.push(serde_json::to_value({arg_name}).map_err(|e| Error::Codec(e.to_string()))?);"
                ));
            }
        }

        let mut lines = vec![];
        if let Some(doc) = &schema.doc {
            for line in doc.lines() {
                lines.push(format!("    /// {line}"));
            }
        }

        match &schema.returns {
            TypeDescriptor::Stream { .. } => {
                lines.push(format!(
                    "    pub async fn {method_name}(&self, {}) -> Result<saikuro::SaikuroStream> {{",
                    params.join(", ")
                ));
                lines.push("        let mut args = Vec::new();".to_owned());
                lines.extend(arg_pushes);
                lines.push(format!(
                    "        self.client.stream({target:?}, args).await"
                ));
                lines.push("    }".to_owned());
            }
            TypeDescriptor::Channel { .. } => {
                lines.push(format!(
                    "    pub async fn {method_name}(&self, {}) -> Result<saikuro::SaikuroChannel> {{",
                    params.join(", ")
                ));
                lines.push("        let mut args = Vec::new();".to_owned());
                lines.extend(arg_pushes);
                lines.push(format!(
                    "        self.client.channel({target:?}, args).await"
                ));
                lines.push("    }".to_owned());
            }
            _ => {
                let return_type = Self::type_to_rust_in_client(&schema.returns);
                lines.push(format!(
                    "    pub async fn {method_name}(&self, {}) -> Result<{return_type}> {{",
                    params.join(", ")
                ));
                lines.push("        let mut args = Vec::new();".to_owned());
                lines.extend(arg_pushes);
                lines.push(format!(
                    "        let value = self.client.call({target:?}, args).await?;"
                ));
                lines.push(format!(
                    "        serde_json::from_value::<{return_type}>(value).map_err(|e| Error::Codec(e.to_string()))"
                ));
                lines.push("    }".to_owned());
            }
        }

        lines.push("".to_owned());
        Ok(lines.join("\n"))
    }

    fn type_to_rust(desc: &TypeDescriptor) -> String {
        convert_type(desc, &RustTypeConverter)
    }

    fn type_to_rust_in_client(desc: &TypeDescriptor) -> String {
        convert_type(desc, &RustClientTypeConverter)
    }
}

struct RustTypeConverter;

impl TypeConverter for RustTypeConverter {
    fn primitive_name(&self, t: &PrimitiveType) -> &'static str {
        match t {
            PrimitiveType::Bool => "bool",
            PrimitiveType::I8 => "i8",
            PrimitiveType::I16 => "i16",
            PrimitiveType::I32 => "i32",
            PrimitiveType::I64 => "i64",
            PrimitiveType::U8 => "u8",
            PrimitiveType::U16 => "u16",
            PrimitiveType::U32 => "u32",
            PrimitiveType::U64 => "u64",
            PrimitiveType::F32 => "f32",
            PrimitiveType::F64 => "f64",
            PrimitiveType::String => "String",
            PrimitiveType::Bytes => "Vec<u8>",
            PrimitiveType::Any => "serde_json::Value",
            PrimitiveType::Unit => "()",
        }
    }

    fn named_type(&self, name: &str) -> String {
        sanitize_type_name_pascal(name)
    }

    fn wrap_option(&self, inner: &str) -> String {
        format!("Option<{inner}>")
    }

    fn wrap_array(&self, inner: &str) -> String {
        format!("Vec<{inner}>")
    }

    fn wrap_map(&self, value: &str) -> String {
        format!("std::collections::HashMap<String, {value}>")
    }

    fn wrap_stream(&self, item: &str) -> String {
        format!("saikuro::SaikuroStream /* item: {item} */")
    }

    fn wrap_channel(&self, inbound: &str, outbound: &str) -> String {
        format!("saikuro::SaikuroChannel /* in: {inbound}, out: {outbound} */")
    }
}

struct RustClientTypeConverter;

impl TypeConverter for RustClientTypeConverter {
    fn primitive_name(&self, t: &PrimitiveType) -> &'static str {
        RustTypeConverter.primitive_name(t)
    }

    fn named_type(&self, name: &str) -> String {
        format!("super::types::{}", sanitize_type_name_pascal(name))
    }

    fn wrap_option(&self, inner: &str) -> String {
        RustTypeConverter.wrap_option(inner)
    }

    fn wrap_array(&self, inner: &str) -> String {
        RustTypeConverter.wrap_array(inner)
    }

    fn wrap_map(&self, value: &str) -> String {
        RustTypeConverter.wrap_map(value)
    }

    fn wrap_stream(&self, item: &str) -> String {
        RustTypeConverter.wrap_stream(item)
    }

    fn wrap_channel(&self, inbound: &str, outbound: &str) -> String {
        RustTypeConverter.wrap_channel(inbound, outbound)
    }
}

fn ensure_unique_name(
    scope: &str,
    raw_name: &str,
    sanitized_name: &str,
    seen: &mut HashMap<String, String>,
) -> Result<String> {
    if let Some(previous_raw) = seen.get(sanitized_name) {
        return Err(CodegenError::Schema(format!(
            "name collision in {scope}: duplicate or conflicting names '{previous_raw}' and '{raw_name}' both resolve to '{sanitized_name}'"
        )));
    }

    seen.insert(sanitized_name.to_owned(), raw_name.to_owned());
    Ok(sanitized_name.to_owned())
}

fn sanitize_ident(s: &str) -> String {
    let mut out: String = s
        .chars()
        .map(|c| if c.is_ascii_alphanumeric() { c } else { '_' })
        .collect();
    if out.is_empty() {
        out.push('_');
    }
    if out == "_" {
        out = "_generated".to_owned();
    }
    if out.chars().next().is_some_and(|c| c.is_ascii_digit()) {
        out.insert(0, '_');
    }
    // Avoid Rust reserved keywords by appending an underscore.
    if RUST_KEYWORDS.contains(&out.as_str()) {
        out.push('_');
    }
    out
}

fn sanitize_pascal_preserve_leading(s: &str) -> String {
    let ident = sanitize_ident(s);
    let leading = ident.starts_with('_');
    let mut body = to_pascal_case(ident.trim_start_matches('_'));
    if body.is_empty() {
        body = "Empty".to_owned();
    }
    if leading {
        format!("_{}", body)
    } else {
        // Avoid reserved keywords
        if RUST_KEYWORDS.contains(&body.as_str()) {
            format!("{}_", body)
        } else {
            body
        }
    }
}

fn sanitize_type_name(s: &str) -> String {
    let ident = sanitize_ident(s);
    if ident.is_empty() {
        "_".to_string()
    } else if ident.chars().next().unwrap().is_ascii_digit() {
        format!("_{}", ident)
    } else {
        // Avoid reserved keywords for type names
        if RUST_KEYWORDS.contains(&ident.as_str()) {
            format!("{}_", ident)
        } else {
            ident
        }
    }
}

fn sanitize_type_name_pascal(s: &str) -> String {
    let ident = sanitize_type_name(s);
    let leading = ident.starts_with('_');
    let mut body = to_pascal_case(ident.trim_start_matches('_'));
    if body.is_empty() {
        body = "Empty".to_owned();
    }
    if leading {
        format!("_{}", body)
    } else {
        body
    }
}

const RUST_KEYWORDS: &[&str] = &[
    "as", "break", "const", "continue", "crate", "else", "enum", "extern", "false", "fn", "for",
    "if", "impl", "in", "let", "loop", "match", "mod", "move", "mut", "pub", "ref", "return",
    "self", "Self", "static", "struct", "super", "trait", "true", "type", "unsafe", "use", "where",
    "while", "async", "await", "dyn", "union", "try",
];
